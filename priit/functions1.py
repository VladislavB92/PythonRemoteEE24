def plus_operation(first_number, second_number):
	operation_result = float(first_number + second_number)
	return operation_result


def minus_operation(first_number, second_number):
	operation_result = float(first_number - second_number)
	return operation_result


def multiply_operation(first_number, second_number):
	operation_result = float(first_number * second_number)
	return operation_result


def divide_operation(first_number, second_number):
	operation_result = float(first_number / second_number)
	return operation_result
